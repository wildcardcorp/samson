        for _ in range(10):
            bits = max(16, Bytes.random(2).int() >> 4)
            rsa  = RSA(bits, e=65537)

            for _ in range(10):
                plaintext  = Bytes.random((bits // 8) - 1)
                ciphertext = rsa.encrypt(plaintext)

